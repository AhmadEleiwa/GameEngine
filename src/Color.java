
public class Color {
    public float r;
    public float g;
    public float b;
    public Color(int r, int g, int b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }
    public void setColor(int r, int g, int b){
        this.r = r;
        this.g = g;
        this.b = b;
    }

}
